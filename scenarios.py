# ─── Lookup helpers ────────────────────────────────────────────────────────────

SCENARIO_BY_ID: dict[str, Scenario] = {s.id: s for s in SCENARIOS}

SCENARIOS_BY_DIFFICULTY: dict[str, list[Scenario]] = {
    "easy":   [s for s in SCENARIOS if s.difficulty == "easy"],
    "medium": [s for s in SCENARIOS if s.difficulty == "medium"],
    "hard":   [s for s in SCENARIOS if s.difficulty == "hard"],
}


def get_scenario(scenario_id: str) -> Scenario:
    if scenario_id not in SCENARIO_BY_ID:
        raise ValueError(f"Unknown scenario ID: {scenario_id}")
    return SCENARIO_BY_ID[scenario_id]
