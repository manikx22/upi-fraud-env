"""
UPI Fraud Scenarios Dataset
12 realistic scam scenarios covering all major fraud types in India.
Each scenario has multiple turns, ground-truth optimal actions, and reward metadata.
"""

from dataclasses import dataclass, field
from typing import Optional
from enum import Enum


class ScamType(str, Enum):
    COLLECT_REQUEST    = "collect_request"
    OTP_THEFT          = "otp_theft"
    FAKE_REFUND        = "fake_refund"
    QR_REVERSAL        = "qr_reversal"
    JOB_FRAUD          = "job_fraud"
    IMPERSONATION      = "impersonation"
    SCREEN_SHARE       = "screen_share"
    PHISHING_LINK      = "phishing_link"


class ActionType(str, Enum):
    # Preventive
    DECLINE_REQUEST      = "decline_request"
    HANG_UP              = "hang_up"
    BLOCK_CONTACT        = "block_contact"
    IGNORE               = "ignore"
    # Recovery
    CALL_BANK_HELPLINE   = "call_bank_helpline"
    RAISE_UPI_DISPUTE    = "raise_upi_dispute"
    REPORT_TO_NPCI       = "report_to_npci"
    FILE_CYBERCRIME      = "file_cybercrime"
    # Verification
    VERIFY_UPI_ID        = "verify_upi_id"
    CHECK_QR_CODE        = "check_qr_code"
    CALL_OFFICIAL_NUMBER = "call_official_number"
    # Information
    SHARE_WITH_FAMILY    = "share_with_family"
    TAKE_SCREENSHOT      = "take_screenshot"
    DO_NOTHING           = "do_nothing"
    # Dangerous (agent should never do these)
    SHARE_OTP            = "share_otp"
    SHARE_PIN            = "share_pin"
    PAY_SCAMMER          = "pay_scammer"
    SCAN_QR              = "scan_qr"


@dataclass
class Turn:
    """A single turn in a scenario — the current situation the agent sees."""
    turn_id: int
    context: str                          # What the user sees / what just happened
    scammer_message: Optional[str]        # What the scammer said/sent (if any)
    optimal_action: ActionType            # Best possible action
    acceptable_actions: list[ActionType]  # Also-OK actions (partial credit)
    dangerous_actions: list[ActionType]   # Never do these (heavy penalty)
    reward_if_optimal: float              # Reward for optimal action
    reward_if_acceptable: float           # Reward for acceptable action
    reward_if_dangerous: float            # Reward for dangerous action (negative)
    explanation: str                      # Why optimal is correct
    # ── Structured signals (parsed, not embedded in context text) ────────────
    vpa_verified: Optional[bool] = None          # True=legit VPA, False=fake, None=unknown
    is_official_channel: Optional[bool] = None   # Is the caller/sender using official channel?
    pressure_tactic: Optional[str] = None        # "urgency"|"threat"|"authority"|"fomo"|None
    call_spoofed: Optional[bool] = None          # Phone number spoofed to look official?
    amount_anomaly: Optional[bool] = None        # Amount differs from expected/historical?
    re_victimisation_attempt: bool = False       # Is this a second-wave scam on same victim?


@dataclass
class Scenario:
    """A complete scam scenario with multiple turns."""
    id: str
    scam_type: ScamType
    title: str
    description: str
    difficulty: str                        # easy / medium / hard
    amount_at_risk: int                    # INR
    amount_lost: int                       # Already lost (0 if not yet)
    user_profile: str                      # Who the victim is
    initial_context: str                   # Starting state description
    turns: list[Turn]
    tags: list[str] = field(default_factory=list)


